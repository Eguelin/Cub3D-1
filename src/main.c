#include "cub3d.h"

int	main(int argc, char **argv)
{
	t_data	data;

	db_ascii_title("");
	if (init_data_struct(&data, argc, argv))
		return (EXIT_FAILURE);
	if (parse_main(&data))
		return (EXIT_FAILURE);
	db_readfile(argv[1]);
	return (EXIT_SUCCESS);
}
